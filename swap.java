public class swap {
    public static void main(String[] args){
        float F1 = 1.20f, F2 = 2.45f;
        System.out.println(F1);
        System.out.println(F2);
        float temporary = F1;
        F1 = F2;
        F2 = temporary;
        System.out.println(F1);
        System.out.println(F2);
    }
    
}
